package second;

public class Op1 {

	public static void main(String[] args) {

		int a = 20, b = 10, c = 0;
		c=a+b;
		
		System.out.println(c);
		
		
		c=a-b;
		System.out.println(c);
		
		c=a*b;
		System.out.println(c);
		
		c=a/b;
		System.out.println(c);
		
		c=a%b;
		System.out.println(c);
	}

}
